|      2 | Encapsulamiento(propiedades privadas) | Mejorar `Personaje` con getters/setters                                  |
|      3 | Herencia e interfaces                  | Derivar `Enemigo` y `Jugador` de `Personaje`; crear `IDañable`           |
|      4 | Polimorfismo y composición             | Implementar distintos tipos de `Enemigo` que reaccionen distinto al daño |

    */
