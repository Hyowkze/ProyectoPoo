| **Polimorfismo**    | Métodos pueden comportarse distinto según el objeto | `TomarDaño()` puede hacer cosas distintas |
| **Abstracción**     | Ocultar detalles complejos                          | Interfaz `IDañable`                       |

    */

    /*
    | Semana | Conceptos                              | Ejercicio                                                                |
| -----: | -------------------------------------- | ------------------------------------------------------------------------ |
